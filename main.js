const submitButton = document.getElementById('submitButton');
const userName = document.querySelector('.fetch-name');
const userCountryOne = document.querySelector('.fetch-country-one');
const userCountryTwo = document.querySelector('.fetch-country-two');
const userProbabilityOne = document.querySelector('.fetch-probability-one');
const userProbabilityTwo = document.querySelector('.fetch-probability-two');
const fetchInfo = document.querySelector('.fetch-info');


submitButton.addEventListener('click', fetchInformation);
document.addEventListener('keydown', function (evt) {
  if (evt.key === "Enter") {
    fetchInformation();
  }
});

function fetchInformation() {
  const userInput = document.getElementById('name-input').value;
  const url = `https://api.nationalize.io/?name=${encodeURIComponent(userInput)}`;
  fetch(url)
    .then((response) => response.json())
    .then((info) => {
      const infoCountryNameOne = info.country[0].country_id;
      const countryName = countryList[infoCountryNameOne] || 'Неизвестная страна';
      const probability = info.country[0].probability;
      renderFirstInfo(userInput, countryName, probability);
      console.log(info);
      const infoCountryNameTwo = info.country[1].country_id;
      const countryNameTwo = countryList[infoCountryNameTwo] || 'Неизвестная страна';
      const probabilityTwo = info.country[1].probability;
      renderSecondInfo(countryNameTwo, probabilityTwo)
    })
    
    .catch((error) => {
      console.error('Ошибка:', error);
    });

};

function renderFirstInfo(name, country, probability) {
  userName.textContent = name + " " + "is from";
  userCountryOne.textContent = country + " " + "with";
  userProbabilityOne.textContent = Math.round(probability * 100) + "%" + " " + "and";
}

function renderSecondInfo(country, probability) {
  userCountryTwo.textContent = country;
  userProbabilityTwo.textContent = Math.round(probability * 100) + "%" + " " + "certainty";
}
